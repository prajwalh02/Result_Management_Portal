    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int rollIndex = 0; // The column index of the Roll No in the table, adjust it according to your table
        String roll = jTable1.getValueAt(selectedRow, rollIndex).toString();

        try {
            // Connecting with the database
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "P@Hole02");
            // Use statement class
            Statement st = con.createStatement();
            st.executeUpdate("DELETE FROM result WHERE rollno = '" + roll + "'");

            JOptionPane.showMessageDialog(null, "Row deleted successfully.");

            // Refresh the table after deletion
            ResultSet rs = st.executeQuery("SELECT * FROM result");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_deleteActionPerformed

