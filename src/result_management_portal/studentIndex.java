    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        
        String rollno = jTextField4.getText();
        
        //calling the drivevr class of sql
        //saying thar i want connection with you
        
        //Connecting with database
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");       
            java.sql.Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "P@Hole02");
            //use statement class
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno ='"+rollno+"' and result.rollno = '"+rollno+"'");   //join the student and result table
            if(rs.next()){
                
                //set the data
                jTextField1.setText(rs.getString(2));   //course name
                jTextField2.setText(rs.getString(3));   //branch name
                jTextField3.setText(rs.getString(4));   //name
                jTextField5.setText(rs.getString(6));   //father name
                jTextField6.setText(rs.getString(5));   //gender
                jTextField7.setText(rs.getString(8));     //DSA
                jTextField8.setText(rs.getString(9));     //maths
                jTextField9.setText(rs.getString(10));    //os
                jTextField10.setText(rs.getString(11));   //dbms
                jTextField11.setText(rs.getString(12));   //toc
                
                // i have to calculate the total marks of all the subject
                //convert into integer then calulate the total marks and then addback to JTExtField12 as an string
                
                int s1 = Integer.parseInt(jTextField7.getText());
                int s2 = Integer.parseInt(jTextField8.getText());
                int s3 = Integer.parseInt(jTextField9.getText());
                int s4 = Integer.parseInt(jTextField10.getText());
                int s5 = Integer.parseInt(jTextField11.getText());
                
                String totalMarks = String.valueOf(s1+s2+s3+s4+s5);
                
                //set back to the jTextField12 as a string
                jTextField12.setText(totalMarks);
            }
            
                      
        }
        catch(Exception e) {
             JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

