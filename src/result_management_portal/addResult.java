    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String dsa = jTextField2.getText();
        String maths = jTextField3.getText();
        String os = jTextField4.getText();
        String dbms = jTextField5.getText();
        String toc = jTextField6.getText();
        
        //calling the drivevr class of sql
        //saying thar i want connection with you
        
        //Connecting with database
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");       
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "P@Hole02");
            
            //use statement class
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno ='"+roll+"'");
            
            if(rs.next()) {
                st.executeUpdate("insert into result(rollno, dsa, maths, os, dbms, toc) values('"+roll+"', '"+dsa+"', '"+maths+"', '"+os+"', '"+dbms+"', '"+toc+"')");
                JOptionPane.showMessageDialog(null, "Marks saved Successfully");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else {
                JOptionPane.showMessageDialog(null, "This Roll number is not Registered");
            }
        }
        catch(Exception e) {
             JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_saveActionPerformed

